const mongoose = require("mongoose");

const facilitySchema = new mongoose.Schema(
  {
    facility: {
      type: String,
      required: true,
    },
    date: {
      type: Date,
      required: true,
    },
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("Facility", facilitySchema);
